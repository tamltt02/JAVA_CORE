    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:

        int count = 0;
        int a = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm không ?");
        if (a == 0) {
            if (txtDiem.getText().isEmpty() || lblKH.getText().isEmpty() || lblNH.getText().isEmpty()) {
                count++;
                JOptionPane.showMessageDialog(this, "Dữ liệu không được null");
                return;
            }
            for (HocVienModel x : hvService.getlist()) {
                if (x.getKhoaHoc().getMaKH() == Integer.parseInt(lblKH.getText()) && x.getNguoiHoc().getMaNH().equals(lblNH.getText())) {
                    count++;
                    JOptionPane.showMessageDialog(this, "Học Viên đã tồn tại");
                    return;
                }
            }
            if (count == 0) {
                HocVienModel edit = getGUI();
                edit.setMaHV(0);
                JOptionPane.showMessageDialog(this, hvService.add(edit));
                loaddata(hvService.getlist());
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if (tblHV.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this, "Mời bạn chọn ");
            return;
        }
        int count = 0;
        int a = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm không ?");
        if (a == 0) {
            if (txtDiem.getText().isEmpty() || lblKH.getText().isEmpty() || lblNH.getText().isEmpty()) {
                count++;
                JOptionPane.showMessageDialog(this, "Dữ liệu không được null");
                return;
            }
            if (count == 0) {
                JOptionPane.showMessageDialog(this, hvService.update(getGUI()));
                loaddata(hvService.getlist());
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

