    private void btnKhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKhActionPerformed
        String a = JOptionPane.showInputDialog("Nhập Mã khóa học :");
        if (!a.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Mã khóa học là ký tự số");
            return;
        }
        for (KhoaHocModel x : khService.getlist()) {
            if (x.getMaKH() == Integer.parseInt(a)) {
                lblKH.setText(a);
            }
        }

    }//GEN-LAST:event_btnKhActionPerformed

