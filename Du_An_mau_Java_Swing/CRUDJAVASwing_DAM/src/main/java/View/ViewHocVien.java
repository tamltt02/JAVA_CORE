    private void btnXuatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXuatActionPerformed
        // TODO add your handling code here:
        XSSFWorkbook workbook = new XSSFWorkbook();
        XSSFSheet sheet = workbook.createSheet("danhsach");
        XSSFRow row = null;
        Cell cell = null;
        row = sheet.createRow(3);
        cell = row.createCell(0, CellType.STRING);
        cell.setCellValue("STT");

        cell = row.createCell(1, CellType.STRING);
        cell.setCellValue("Mã Học Viên");
        cell = row.createCell(2, CellType.STRING);
        cell.setCellValue("Mã Khóa Học");
        cell = row.createCell(3, CellType.STRING);
        cell.setCellValue("Tên Người học");
        cell = row.createCell(4, CellType.STRING);
        cell.setCellValue("Điểm ");
        try {
            for (int i = 0; i < hvService.getlist().size(); i++) {
                FileOutputStream fis = null;
                row = sheet.createRow(5 + i);
                cell = row.createCell(0, CellType.NUMERIC);
                cell.setCellValue(i + 1);
                cell = row.createCell(1, CellType.STRING);
                cell.setCellValue(hvService.getlist().get(i).getMaHV());
                cell = row.createCell(2, CellType.STRING);
                cell.setCellValue(hvService.getlist().get(i).getKhoaHoc().getMaKH());
                cell = row.createCell(3, CellType.STRING);
                cell.setCellValue(getTenNH(hvService.getlist().get(i).getNguoiHoc().getMaNH()));
                cell = row.createCell(4, CellType.STRING);
                cell.setCellValue(hvService.getlist().get(i).getDiem());
                File f = new File("D://danhsach.xlsx");
                fis = new FileOutputStream(f);
                workbook.write(fis);
                fis.close();

            }
            JOptionPane.showMessageDialog(this, "Xuất thành công");
        } catch (Exception ex) {
            Logger.getLogger(ViewHocVien.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Xuất không thành công");

        }


    }//GEN-LAST:event_btnXuatActionPerformed


