    String getTenNH(String maNH) {
        for (NguoiHocModel nh : nhService.getlist()) {
            if (nh.getMaNH().equals(maNH)) {
                return nh.getHoTen();
            }
        }
        return null;
    }

    NguoiHoc getNH(String maNH) {
        for (NguoiHocModel nh : nhService.getlist()) {
            if (nh.getMaNH().equals(maNH)) {
                return new NguoiHoc(maNH, nh.getHoTen(), nh.getNgaySinh(), nh.isGioiTinh(), nh.getDienThoai(), nh.getEmail(), nh.getGhiChu(), nh.getNhanVien(), nh.getNgayDK(), null);
            }
        }
        return null;
    }

    KhoaHoc getKH(int maKH) {
        for (KhoaHocModel kh : khService.getlist()) {
            if (kh.getMaKH() == maKH) {
                return new KhoaHoc(maKH, kh.getHocPhi(), kh.getThoiLuong(), kh.getNgayKG(), kh.getGhiChu(), kh.getNgayTao(), kh.getNhanvien(), kh.getChuyenDe(), null);
            }
        }
        return null;
    }

    String getmaNH(String tenNH) {
        for (NguoiHocModel nh : nhService.getlist()) {
            if (nh.getHoTen().equals(tenNH)) {
                return nh.getMaNH();
            }
        }
        return null;
    }

    void loaddata(List<HocVienModel> list) {

        _dTableModel = (DefaultTableModel) tblHV.getModel();
        _dTableModel.setRowCount(0);
        for (HocVienModel x : list) {
            _dTableModel.addRow(new Object[]{x.getMaHV(),
                x.getKhoaHoc().getMaKH(),
                x.getNguoiHoc().getHoTen(),
                x.getDiem()});
        }
    }

    void showdetail(int index) {
        txtmaHV.setText(tblHV.getModel().getValueAt(index, 0).toString());
        txtDiem.setText(tblHV.getModel().getValueAt(index, 3).toString());
        lblKH.setText(tblHV.getModel().getValueAt(index, 1).toString());
        lblNH.setText(getmaNH(tblHV.getModel().getValueAt(index, 2).toString()));

    }

    HocVienModel getGUI() {
        return new HocVienModel(Integer.parseInt(txtmaHV.getText()),
                getKH(Integer.parseInt(lblKH.getText())),
                getNH(lblNH.getText()),
                Double.parseDouble(txtDiem.getText()));
    }

