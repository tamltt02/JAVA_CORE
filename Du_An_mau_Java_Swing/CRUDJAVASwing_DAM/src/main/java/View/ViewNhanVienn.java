    void loaddata(List<NhanVienModel> list) {
        _dTableModel = (DefaultTableModel) tblNV.getModel();
        _dTableModel.setRowCount(0);
        for (NhanVienModel x : list) {
            _dTableModel.addRow(new Object[]{x.getmaNV(), x.getmatKhau(), x.gethoTen(), x.isvaiTro()? "Trưởng Phòng" : "Nhân Viên"});
        }
    }

    void btn() {
        ButtonGroup btn = new ButtonGroup();
        btn.add(rdbNhanVien);
        btn.add(rdbTruongPhong);
        rdbTruongPhong.setSelected(true);
    }

    NhanVienModel getGUI() {
        return new NhanVienModel(txtMaNV.getText(), txtMatKhau.getText(), txtHoTen.getText(), rdbTruongPhong.isSelected() ? true : false);
    }

    void showdetail(int index) {
        txtMaNV.setText(tblNV.getModel().getValueAt(index, 0).toString());
        txtMatKhau.setText(tblNV.getModel().getValueAt(index, 1).toString());
        txtHoTen.setText(tblNV.getModel().getValueAt(index, 2).toString());
        rdbTruongPhong.setSelected(tblNV.getModel().getValueAt(index, 3).equals("Trưởng Phòng") ? true : false);
        rdbNhanVien.setSelected(tblNV.getModel().getValueAt(index, 3).equals("Nhân Viên") ? true : false);
    }

