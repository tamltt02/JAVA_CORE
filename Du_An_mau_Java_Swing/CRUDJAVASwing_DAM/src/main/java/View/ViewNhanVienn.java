    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (tblNV.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this, "Mời bạn chọn ");
            return;
        }
        int a = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không ?");
        if (a == 0) {
            JOptionPane.showMessageDialog(this, nvServices.delete(getGUI()));
            loaddata(nvServices.getlist());
            btnClearActionPerformed(evt);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        int count = 0;
        int a = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm không ?");
        if (a == 0) {
            for (NhanVienModel x : nvServices.getlist()) {
                if (x.getmaNV().equals(txtMaNV.getText())) {
                    count++;
                    JOptionPane.showMessageDialog(this, "Trùng mã");
                    return;
                }
            }
              for (NhanVienModel x : nvServices.getlist()) {
                if (x.getmatKhau().equals(txtMatKhau.getText())) {
                    count++;
                    JOptionPane.showMessageDialog(this, "Mật khẩu không được trùng ");
                    return;
                }
            }
            if (txtMaNV.getText().isEmpty() || txtHoTen.getText().isEmpty() || txtMatKhau.getText().isEmpty()) {
                count++;
                JOptionPane.showMessageDialog(this, "Dữ liệu không được null");
                return;
            }
            if (count == 0) {
                JOptionPane.showMessageDialog(this, nvServices.add(getGUI()));
                loaddata(nvServices.getlist());
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:

        if (tblNV.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this, "Mời bạn chọn ");
            return;
        }
        int count = 0;
        int a = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa không ?");
        if (a == 0) {
            if (txtMaNV.getText().isEmpty() || txtHoTen.getText().isEmpty() || txtMatKhau.getText().isEmpty()) {
                count++;
                JOptionPane.showMessageDialog(this, "Dữ liệu không được null");
                return;
            }
            if (count == 0) {
                JOptionPane.showMessageDialog(this, nvServices.update(getGUI()));
                loaddata(nvServices.getlist());
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

