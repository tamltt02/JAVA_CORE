    void loadTable(List<ChucVu> list) {
        if (list.isEmpty()) {
            _dTableModel.setRowCount(0);
            return;
        }
        _dTableModel = (DefaultTableModel) tbl_cv.getModel();
        _dTableModel.setRowCount(0);
        int stt = 1;
        for (ChucVu x : list) {
            _dTableModel.addRow(new Object[]{stt, x.getMa(), x.getTen()});
            stt++;
        }
    }

