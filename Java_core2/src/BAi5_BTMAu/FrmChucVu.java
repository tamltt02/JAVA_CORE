    public void timSP(JTextField txt, JTable tbl) {
        _dTableModel = (DefaultTableModel) tbl.getModel();
        _dTableModel.fireTableDataChanged();
        TableRowSorter Sorter = new TableRowSorter(_dTableModel);
        tbl.setRowSorter(Sorter);
        Sorter.setRowFilter(RowFilter.regexFilter(txt.getText()));
    }
