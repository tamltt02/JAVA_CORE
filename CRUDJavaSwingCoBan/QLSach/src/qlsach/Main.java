    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
        int index = tbl_sach.getSelectedRow();
        if(index < 0){
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn ");
        }
        int count = 0;
        if (txt_ma.getText().isEmpty() || txt_ten.getText().isEmpty() || txt_nxb.getText().isEmpty() || txt_sl.getText().isEmpty() || txt_gia.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Dữ liệu không được null");
            count++;
            return;
        }
        if (Integer.parseInt(txt_sl.getText()) < 0) {
            JOptionPane.showMessageDialog(this, "Số lượng > 0");
            count++;
            return;
        }
        if (Float.parseFloat(txt_gia.getText()) < 0) {
            JOptionPane.showMessageDialog(this, "Giá tiền > 0");
            count++;
            return;
        }
        if (count == 0) {
            sua();
        }
    }//GEN-LAST:event_btn_suaActionPerformed

