    void sua() {
        int a = JOptionPane.showConfirmDialog(this, "Bạn có muốn sua không?");
        if (a == 0) {
            try {
                String sql = "UPDATE sach set tensach=?,matheloai =?,nxb=?,soluong=?,giatien=? where masach =? ";
                _ps = conn.prepareStatement(sql);
                _ps.setString(1, txt_ten.getText());
                _ps.setString(2, cbc_tl.getSelectedItem() + "");
                _ps.setString(3, txt_nxb.getText());
                _ps.setString(4, txt_sl.getText());
                _ps.setString(5, txt_gia.getText());
                _ps.setString(6, txt_ma.getText());
                int b = _ps.executeUpdate();
                if (b > 0) {
                    loadata(getlistdata());
                    JOptionPane.showMessageDialog(this, "Sửa thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Sửa thất bại");
                }
                _ps.close();
            } catch (SQLException ex) {
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
