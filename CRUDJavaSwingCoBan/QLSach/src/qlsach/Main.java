    void cbctheloai() {
        try {
            cbc_tl.removeAllItems();
            String sql = "select * from theloai";
            _st = conn.createStatement();
            ResultSet rs = _st.executeQuery(sql);
            while (rs.next()) {
                cbc_tl.addItem(rs.getString(1));

            }
            cbc_tl.setSelectedIndex(2);
            _st.close();
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    String getten(String ma) {
        try {
            String sql = "select * from theloai";
            _st = conn.createStatement();
            ResultSet rs = _st.executeQuery(sql);
            while (rs.next()) {
                if (rs.getString(1).equals(ma)) {
                    return rs.getString(2);
                }
            }

            _st.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
        return null;
    }

    List<sach> getlistdata() {
        try {
            List<sach> list = new ArrayList();
            String sql = "select masach,tensach,matheloai,nxb,soluong,giatien from sach where matheloai =?";
            _ps = conn.prepareStatement(sql);
            _ps.setString(1, cbc_tl.getSelectedItem() + "");
            ResultSet rs = _ps.executeQuery();
            while (rs.next()) {
                list.add(new sach(rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getInt(5), rs.getFloat(6)));

            }
            _ps.close();
            return list;

        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            return null;
        }
    }

    void loadata(List<sach> list) {
        _dTableModel = (DefaultTableModel) tbl_sach.getModel();
        _dTableModel.setRowCount(0);
        for (sach x : list) {
            _dTableModel.addRow(new Object[]{x.getMasach(), x.getTensach(), x.getMaTL(), x.getNxb(), x.getSoluong(), x.getGiaban()});
        }
    }

