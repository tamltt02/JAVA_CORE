    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
        int index = tbl_cv.getSelectedRow();
        int count = 0;
        if (_uUtiliti.checkSo(txt_ten.getText())  || txt_ten.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mời bạn nhập lại tên");
            count++;
        }
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Bạn phải chọn");
            return;
        }
        if (count == 0) {
            int a = JOptionPane.showConfirmDialog(this, "Bạn có muốn UPDATE không?");
            if (a == 0) {
                JOptionPane.showMessageDialog(this, _cCVservice.store(getGUI(), "UPDATE"));
                Loaddata(_cCVservice.getlist());
            }
        }

    }//GEN-LAST:event_btn_suaActionPerformed

    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        int count = 0;
        txt_ma.setEnabled(true);

        if (txt_ma.getText().isEmpty() || _cCVservice.getIndexByMa(txt_ma.getText()) != -3) {
            JOptionPane.showMessageDialog(this, "Mời bạn nhập lại mã");
            count++;
        }
        if (_uUtiliti.checkSo(txt_ten.getText()) || txt_ten.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mời bạn nhập lại tên");
            count++;
        }
        
        
        if (count == 0) {
            int a = JOptionPane.showConfirmDialog(this, "Bạn có muốn INSERT không");
            if (a == 0) {
                JOptionPane.showMessageDialog(this, _cCVservice.store(getGUI(), "INSERT"));
            }
            Loaddata(_cCVservice.getlist());
        }
    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        int index = tbl_cv.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Bạn phải chọn");
            return;
        }
 for (int i = 0; i < _nvService.getlist().size(); i++) {
             if(txt_ma.getText().equals(getSP(_nvService.getlist().get(i).getIdCV()))){
             JOptionPane.showMessageDialog(this, "Bạn phải xóa ở bảng SP");
              return;
          }
        
        int a = JOptionPane.showConfirmDialog(this, "Bạn có muốn DELETE không");
        if (a == 0) {
            JOptionPane.showMessageDialog(this, _cCVservice.store(getGUI(), "DELETE"));
            Loaddata(_cCVservice.getlist());
            btn_clearActionPerformed(evt);
        }
        return;
 }


    }//GEN-LAST:event_btn_xoaActionPerformed

