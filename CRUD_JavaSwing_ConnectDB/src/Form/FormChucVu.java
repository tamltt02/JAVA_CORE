    void Loaddata(List<ChucVu> list) {
        if (list.isEmpty()) {
            _dModel.setRowCount(0);
            return;
        }
        _dModel = (DefaultTableModel) tbl_cv.getModel();
        _dModel.setRowCount(0);

        int stt = 1;
        for (ChucVu x : list) {

            _dModel.addRow(new Object[]{stt, x.getMa(), x.getTen()});
            stt++;
        }
    }

