    void addCBC() {
        cbc_dongsp.removeAllItems();
        for (DongSP x : _dPService.getlist()) {
            cbc_dongsp.addItem(x.getTen());
        }
        if (cbc_dongsp.getItemCount() == 1) {
            cbc_dongsp.setSelectedIndex(0);
        }
    }

    int getidDongSP(String txt) {
        for (DongSP x : _dPService.getlist()) {
            if (x.getTen().equals(txt)) {
                return x.getId();
            }
        }
        return -1;
    }

    String getTenDongSP(int a) {
        for (DongSP x : _dPService.getlist()) {
            if (x.getId() == (a)) {
                return x.getTen();
            }
        }
        return null;
    }
    
    String getmasp(int a) {
        for (SanPham x : _spService.getlist()) {
            if (x.getId() == a) {
                return x.getMa();
            }
        }
        return null;
    }
    
     void loaddata(List<SanPham> lits) {
        addCBC();
        if (lits.isEmpty()) {
            return;
        }
        _dDefaultTableModel = (DefaultTableModel) tbl_sp.getModel();
        _dDefaultTableModel.setRowCount(0);
        int stt = 1;
        for (SanPham x : lits) {
            _dDefaultTableModel.addRow(new Object[]{stt,
                x.getMa(),
                x.getTen(),
                x.getNamBaoHanh(),
                x.getTrongLuong(),
                x.getMota(),
                x.getSLSPton(),
                x.getGiaNhap(),
                x.getGiaBan(),
                getTenDongSP(x.getIdDSP())});
            stt++;
        }

    }

    SanPham getGUI() {
        return new SanPham(-1, txt_ma.getText(), txt_ten.getText(), Integer.parseInt(txt_nam.getText()), Float.parseFloat(txt_trongluong.getText()), txt_mota.getText(), Integer.parseInt(txt_slton.getText()), Float.parseFloat(txt_gianhap.getText()), Float.parseFloat(txt_giaban.getText()), getidDongSP(cbc_dongsp.getItemAt(cbc_dongsp.getSelectedIndex())));
    }

