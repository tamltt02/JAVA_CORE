    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:

        int count = 0;
        if (txt_ma.getText().isEmpty() || txt_ten.getText().isEmpty() || txt_nam.getText().isEmpty() || txt_trongluong.getText().isEmpty() || txt_mota.getText().isEmpty() || txt_slton.getText().isEmpty() || txt_gianhap.getText().isEmpty() || txt_giaban.getText().isEmpty()) {
            count++;
            JOptionPane.showMessageDialog(this, "Dữ liệu không được null mời bạn nhập lại");
            return;
        }
        if (!_utUtiliti.checkSo(txt_nam.getText()) || !_utUtiliti.checkSo(txt_trongluong.getText()) || !_utUtiliti.checkSo(txt_giaban.getText()) || !_utUtiliti.checkSo(txt_gianhap.getText()) || _utUtiliti.checkSo(txt_ten.getText())) {
            count++;
            JOptionPane.showMessageDialog(this, "Năm, trọng lượng và giá phải là số.Tên là chữ. Mời bạn nhập lại");
            return;
        }
        if (_spService.getIndexByMa(txt_ma.getText()) != -3) {
            JOptionPane.showMessageDialog(this, "Mã trùng");
            return;
        }
        if (count == 0) {
            int a = JOptionPane.showConfirmDialog(this, " Bạn có muốn INSERT không?");
            if (a == 0) {
                JOptionPane.showMessageDialog(this, _spService.store(getGUI(), "INSERT"));
                loaddata(_spService.getlist());
            }
        }
    }//GEN-LAST:event_btn_themActionPerformed

    private void tbl_spMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_spMouseClicked
        // TODO add your handling code here:
        txt_ma.setEnabled(false);
        int index = tbl_sp.getSelectedRow();
        txt_ma.setText(tbl_sp.getModel().getValueAt(index, 1).toString());
        txt_ten.setText(tbl_sp.getModel().getValueAt(index, 2).toString());
        txt_nam.setText(tbl_sp.getModel().getValueAt(index, 3).toString());
        txt_trongluong.setText(tbl_sp.getModel().getValueAt(index, 4).toString());
        txt_mota.setText(tbl_sp.getModel().getValueAt(index, 5).toString());
        txt_slton.setText(tbl_sp.getModel().getValueAt(index, 6).toString());
        txt_gianhap.setText(tbl_sp.getModel().getValueAt(index, 7).toString());
        txt_giaban.setText(tbl_sp.getModel().getValueAt(index, 8).toString());
        for (int i = 0; i < cbc_dongsp.getItemCount(); i++) {
            if (getTenDongSP(_spService.getlist().get(index).getIdDSP()).equalsIgnoreCase(cbc_dongsp.getItemAt(i))) {
                cbc_dongsp.setSelectedIndex(i);
            }
        }
    }//GEN-LAST:event_tbl_spMouseClicked

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        addCBC();
        
        int index = tbl_sp.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Bạn phải chọn");
            return;
        }
        for (int i = 0; i < _hdCTService.getlist().size(); i++) {
            if(_spService.getlist().get(index).getMa().equalsIgnoreCase(getmasp(_hdCTService.getlist().get(i).getIdSanPham()))){
                System.out.println(_hdCTService.getlist().get(i).getIdSanPham());
                JOptionPane.showMessageDialog(this, "Bạn cần xóa sản phẩm trong giỏ hàng");
                return;
            }
        }
        int a = JOptionPane.showConfirmDialog(this, " Bạn có muốn DELETE không?");
        if (a == 0) {
            JOptionPane.showMessageDialog(this, _spService.store(getGUI(), "DELETE"));
            loaddata(_spService.getlist());
            btn_clearActionPerformed(evt);
        }

    }//GEN-LAST:event_btn_xoaActionPerformed

